// Escreva um programa que receba um número inteiro e determine se ele é par ou ímpar.

//para um numero ser identificado como PAR ele precisa ser divisivel por 2 
//Um numero não divisivel por 2 é considerado numero IMPAR

const numero = 601

if(numero % 2 === 0){
 console.log("Este numero é PAR")
} else {
    console.log("Este numero é IMPAR")
}

