// Escreva um programa que receba um número inteiro e exiba a tabuada desse número de 1 a 10.

const numero = 6;

console.log(`A tabuada de ${numero}: `)
for(let i = 1 ; i <=10 ; i++){
    console.log(`${numero} x ${i} = ${numero * i}`)
}

